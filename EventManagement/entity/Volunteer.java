package entity;

/**
 *
 * @author KJ
 */
public class Volunteer {
    private String id;
    private String name;
    private String email;

    public Volunteer(String id, String name, String email) {
        this.id = id;
        this.name = name;
        this.email = email;
    }

    // Getters and Setters
    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    @Override
    public String toString() {
        return "Volunteer ID: " + id + ", Name: " + name + ", Email: " + email;
    }

    @Override
    public boolean equals(Object obj) {
        if (this == obj) return true;
        if (obj == null || getClass() != obj.getClass()) return false;

        Volunteer volunteer = (Volunteer) obj;

        return id.equals(volunteer.id);
    }

    @Override
    public int hashCode() {
        return id.hashCode();
    }
}
